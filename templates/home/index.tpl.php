                <h2>DESAFIO TÉCNICO</h2>

                
                <h3>Anoreg (o cliente)</h3>
                É uma associação criada para representar os tabeliães de cartórios em qualquer instância ou Tribunal.<br/>
                Representa os interesses em comum da classe.<br/>
                Também trabalham com cursos e eventos.<br/>
                São financiados por contribuições dos cartórios de todo o Brasil.<br/>
                O Conselho Nacional de Justiça (CNJ) regulamenta e fiscaliza o trabalho dos cartórios.<br/>
                Como eles possuem os cartórios cadastrados, enviam para a Anoreg.<br/><br/>

                <h3>O problema</h3>
                A Anoreg possui uma base dos cartórios associados em planilha Excel.<br/>
                A planilha é atualizada mensalmente com base em um arquivo XML enviado pelo CNJ. A funcionária da Anoreg abre o arquivo XML em um navegador e atualiza a planilha copiando e colando os dados. <br/>
                Os campos telefone e e-mail não são informados pelo CNJ, sendo atualizados manualmente na planilha. <br/>
                Existem cartórios que não são enviados pelo CNJ sendo inseridos manualmente na planilha. <br/>
                A Anoreg, periodicamente, envia comunicados para seus associados endereçados aos e-mails cadastrados na planilha. <br/>
                A Anoreg conta com uma funcionária dedicada exclusivamente para realizar toda essa operação de atualização e envio dos e-mails. <br/>
                Visando reduzir custos, o presidente da Anoreg ordenou que a funcionária seja demitida, e a operação deverá ser realizada pela secretária que, por sua vez, tem diversas outras ações diárias a fazer e não conseguirá realizar o trabalho da forma como é feito.<br/>
                
                <h3>Restrições técnicas</h3>
                O sistema deve ser desenvolvido com base nas tecnologias<br/><br/>

                Apache<br/>
                PHP<br/>
                MySQL<br/>
                HTML/CSS<br/>
                JavaScript<br/><br/>
                
                <b>Frameworks permitidos no desafio:</b><br/>
                JavaScript: jQuery<br/>
                CSS: Bootstrap<br/>
                Framework PHP próprio<br/>
                Pode ser utilizado o Composer
